package java_basic.DI;

// 用户实体类
class User {
    private Long id;
    private String username;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }
// 构造函数、Getter和Setter方法等省略，为简化示例
}